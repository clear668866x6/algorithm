void solve() {
    int h, d;
    cin >> h >> d;

    int ans = 0;

    auto check = [&](int x) {
        int len = d / (x + 1), r = d % (x + 1);
        int tot = (len + 1) * len / 2 * (x - r + 1) + (len + 1) * (len + 2) / 2 * r;
        return h + x >= tot + 1;
    };

    int l = -1, r = d + 1;

    while (l + 1 != r) {
        int mid = (l + r) >> 1;
        if (check(mid))
            r = mid;
        else
            l = mid;
    }

    cout << d + r << endl;
}

signed main() {
    int Task = 1;

    ios::sync_with_stdio(false);
    cin.tie(nullptr);
    cin >> Task;
    while (Task--) {
        solve();
    }

    return 0;
}
