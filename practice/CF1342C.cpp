void solve() {
    int a, b, q;
    cin >> a >> b >> q;

    if (a > b) swap(a, b);

    int t = lcm(a, b);

    auto calc = [&](int x) {
        int t1 = x / t, t2 = x % t;
        if (t2 < b) {
            return (x - t1 * b - t2);
        } else {
            return (x - t1 * b - b + 1);
        }
    };

    while (q--) {
        int l, r;
        cin >> l >> r;
        int tot = 0;
        cout << calc(r) - calc(l - 1) << " \n"[!q];
    }
}

