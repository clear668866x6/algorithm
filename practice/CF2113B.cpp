void solve () {
    int w, h, a, b;
    cin >> w >> h >> a >> b;
    int x1, x2, y1, y2;
    cin >> x1 >> y1 >> x2 >> y2;

    int dx = abs (x1 - x2) % a, dy = abs (y1 - y2) % b;
    if (!dx) {
        if (!dy || x1 != x2) {
            YES;
        } else {
            NO;
        }
    } else {
        if (!dy && y1 != y2) {
            YES;
        } else {
            NO;
        }
    }
}

