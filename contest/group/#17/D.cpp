void solve() {
    int n, k;
    cin >> n >> k;

    int l = 0, r = n + 2;

    function<int(int)> check = [&](int x) { return abs((k + k + x - 1) * x - (k + k + n - 1) * n / 2); };

    while (r - l > 12) {
        int mid1 = l + (r - l) / 3, mid2 = r - (r - l) / 3;
        if (check(mid1) > check(mid2))
            l = mid1;
        else
            r = mid2;
    }

    int ans = 1e18;

    FOR(i, l, r) {
        ans = min(ans, check(i));
    }

    cout << ans << endl;
}

signed main() {
    int Task = 1;

    ios::sync_with_stdio(false);
    cin.tie(nullptr);
    cin >> Task;
    while (Task--) {
        solve();
    }

    return 0;
}
